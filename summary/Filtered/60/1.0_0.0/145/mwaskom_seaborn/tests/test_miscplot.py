import matplotlib.pyplot as plt

from seaborn import miscplot as misc
from seaborn.palettes import color_palette
from .test_utils import _network


class TestPalPlot:
    """Test the function that visualizes a color palette."""
    def test_palplot_size(self):
        """
        Test the size of the palplot function.
        
        This function checks the size of the figure generated by the palplot function
        for different numbers of colors and custom sizes.
        
        Parameters:
        None
        
        Returns:
        None
        
        Assertions:
        - The size of the figure for 4 colors should be (4, 1).
        - The size of the figure for 5 colors should be (5, 1).
        - The size of the figure for 3 colors with a specified size of
        """


        pal4 = color_palette("husl", 4)
        misc.palplot(pal4)
        size4 = plt.gcf().get_size_inches()
        assert tuple(size4) == (4, 1)

        pal5 = color_palette("husl", 5)
        misc.palplot(pal5)
        size5 = plt.gcf().get_size_inches()
        assert tuple(size5) == (5, 1)

        palbig = color_palette("husl", 3)
        misc.palplot(palbig, 2)
        sizebig = plt.gcf().get_size_inches()
        assert tuple(sizebig) == (6, 2)


class TestDogPlot:

    @_network(url="https://github.com/mwaskom/seaborn-data")
    def test_dogplot(self):
        """
        Generate a dog plot using matplotlib.
        
        This function creates a simple dog plot and asserts that the resulting plot contains exactly one image.
        
        Parameters:
        None
        
        Returns:
        None
        
        Assertions:
        - The function asserts that the plot contains exactly one image.
        
        Usage:
        test_dogplot()
        """

        misc.dogplot()
        ax = plt.gca()
        assert len(ax.images) == 1
