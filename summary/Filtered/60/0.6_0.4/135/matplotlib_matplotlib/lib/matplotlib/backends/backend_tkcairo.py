import sys

import numpy as np

from . import _backend_tk
from .backend_cairo import cairo, FigureCanvasCairo
from ._backend_tk import _BackendTk, FigureCanvasTk


class FigureCanvasTkCairo(FigureCanvasCairo, FigureCanvasTk):
    def draw(self):
        """
        Draws the figure and converts it to a numpy array for display.
        
        This function is responsible for rendering the figure using Cairo and then converting the rendered image into a numpy array. The array is then used to update the Tkinter photo image for display.
        
        Parameters:
        None (the function uses instance variables and attributes)
        
        Returns:
        None (modifies the Tkinter photo image in place)
        
        Key Attributes Used:
        - `self.figure`: The figure to be drawn.
        - `self._renderer
        """

        width = int(self.figure.bbox.width)
        height = int(self.figure.bbox.height)
        surface = cairo.ImageSurface(cairo.FORMAT_ARGB32, width, height)
        self._renderer.set_context(cairo.Context(surface))
        self._renderer.dpi = self.figure.dpi
        self.figure.draw(self._renderer)
        buf = np.reshape(surface.get_data(), (height, width, 4))
        _backend_tk.blit(
            self._tkphoto, buf,
            (2, 1, 0, 3) if sys.byteorder == "little" else (1, 2, 3, 0))


@_BackendTk.export
class _BackendTkCairo(_BackendTk):
    FigureCanvas = FigureCanvasTkCairo
