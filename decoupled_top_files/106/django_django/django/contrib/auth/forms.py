class UsernameField(forms.CharField):
    def to_python(self, value):
        return unicodedata.normalize("NFKC", super().to_python(value))

    def widget_attrs(self, widget):
        """
        Generate HTML attributes for a widget.
        
        This function extends the attributes generated by the superclass's
        `widget_attrs` method by adding specific HTML attributes:
        - `autocapitalize`: Set to 'none' to prevent automatic capitalization of
        input text.
        - `autocomplete`: Set to 'username' to enable autocomplete for username
        fields.
        
        Args:
        widget (Widget): The widget for which to generate attributes.
        
        Returns:
        dict: A dictionary containing the
        """

        return {
            **super().widget_attrs(widget),
            "autocapitalize": "none",
            "autocomplete": "username",
        }


class UserCreationForm(forms.ModelForm):
    """
    A form that creates a user, with no privileges, from the given username and
    password.
    """

    error_messages = {
        "password_mismatch": _("The two password fields didn’t match."),
    }
    password1 = forms.CharField(
        label=_("Password"),
        strip=False,
        widget=forms.PasswordInput(attrs={"autocomplete": "new-password"}),
        help_text=password_validation.password_validators_help_text_html(),
    )
    password2 = forms.CharField(
        label=_("Password confirmation"),
        widget=forms.PasswordInput(attrs={"autocomplete": "new-password"}),
        strip=False,
        help_text=_("Enter the same password as before, for verification."),
    )

    class Meta:
        model = User
        fields = ("username",)
        field_classes = {"username": UsernameField}

    def __init__(self, *args, **kwargs):
        """
        Initializes a form with the specified arguments and keyword arguments. Sets the autofocus attribute on the username field if it exists in the form fields.
        
        Args:
        *args: Variable length argument list.
        **kwargs: Arbitrary keyword arguments.
        
        Returns:
        None
        """

        super().__init__(*args, **kwargs)
        if self._meta.model.USERNAME_FIELD in self.fields:
            self.fields[self._meta.model.USERNAME_FIELD].widget.attrs[
                "autofocus"
            ] = True

    def clean_password2(self):
        """
        Validates that the two password entries match.
        
        Args:
        self: The form instance.
        
        Returns:
        password2: The second password entered by the user, if both passwords match.
        
        Raises:
        ValidationError: If the two passwords do not match.
        """

        password1 = self.cleaned_data.get("password1")
        password2 = self.cleaned_data.get("password2")
        if password1 and password2 and password1 != password2:
            raise ValidationError(
                self.error_messages["password_mismatch"],
                code="password_mismatch",
            )
        return password2

    def _post_clean(self):
        """
        Validates the password after updating the instance with form data. Calls the superclass's _post_clean method first. If a password is provided, it validates the password using `password_validation.validate_password` and adds any validation errors to the 'password2' field.
        """

        super()._post_clean()
        # Validate the password after self.instance is updated with form data
        # by super().
        password = self.cleaned_data.get("password2")
        if password:
            try:
                password_validation.validate_password(password, self.instance)
            except ValidationError as error:
                self.add_error("password2", error)

    def save(self, commit=True):
        """
        Saves the provided user instance with the given password. If `commit` is True, the user is saved to the database. The function sets the password using `set_password` method and returns the saved user instance.
        
        Args:
        commit (bool): Whether to save the user instance to the database.
        
        Returns:
        User: The saved user instance.
        """

        user = super().save(commit=False)
        user.set_password(self.cleaned_data["password1"])
        if commit:
            user.save()
        return user


